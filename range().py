# to print 10 numbers(0-9) as a list we use range() :

numbers = list(range(10))
print(numbers)

# specify the begining and end of range :

num = list(range(5, 15))
print(num)      # from 5 to 14

# intervals in range() :
list = list(range(1, 21, 2))    # here it increments 2 steps rather than 1
print(list)

