                        # String Functions :

# join() : it joins items of a list with a particular symbol :
print(",".join(["apple", "banana", "orange"]))

# replace() : to replace certain old element with new element:
print("hello world".replace("world", "John"))

# startswith() - to check a string starts with a particular element
newstr = "my name is"
print(newstr.startswith("my"))

# endwith() - to check a string ends with a particular element
print(newstr.endswith("is"))

# to convert a string into uppercase :
print(newstr.upper())

# similarlly we have lower() to convert into lowercase:
print(newstr.lower())
