# BITWISE OPERAORS :

# complement operator (~):
print(~12)

# bitwise AND operator (&) :
print(12 & 13)      # if both are 1 then only it gives 1 in binary .

# bitwise OR operator (|) :
print(12 | 13)      # if any one is 1 then it gives 1 in binary .

# bitwise XOR operator () :
# if bith are different in binary (like 01 or 10) then it gives 1 else it gives 0.
print(12 ^ 13)

# left shift operator (<<) :
print(10 << 2)
# 1010.000  ---->  101000.0

# right shift operator (>>) :
print(10 >> 2)
# 1010.000  ----->  10.000
